(() => {
  const layer = $('#cursor-layer');
  if (!layer) return;

  const COUNT = 8;
  const SPEED_MAIN = 0.35;
  const SPEED_TAIL = 0.18;

  const dots = [];
  for (let i = 0; i < COUNT; i++) {
    const el = document.createElement('div');
    el.className = i === 0 ? 'cursor-dot main' : 'cursor-dot trail';
    el.style.opacity = i === 0 ? 1 : 0.22 - i * 0.02;
    layer.appendChild(el);
    dots.push({ el, x: 0, y: 0 });
  }

  let tx = innerWidth / 2;
  let ty = innerHeight / 2;

  addEventListener('mousemove', (e) => {
    tx = e.clientX;
    ty = e.clientY;
  });

  const tick = () => {
    dots[0].x += (tx - dots[0].x) * SPEED_MAIN;
    dots[0].y += (ty - dots[0].y) * SPEED_MAIN;
    dots[0].el.style.transform = `translate(${dots[0].x}px, ${dots[0].y}px) translate(-50%, -50%)`;

    for (let i = 1; i < dots.length; i++) {
      dots[i].x += (dots[i - 1].x - dots[i].x) * SPEED_TAIL;
      dots[i].y += (dots[i - 1].y - dots[i].y) * SPEED_TAIL;
      dots[
        i
      ].el.style.transform = `translate(${dots[i].x}px, ${dots[i].y}px) translate(-50%, -50%)`;
    }

    requestAnimationFrame(tick);
  };

  tick();
})();

