document.addEventListener('DOMContentLoaded', () => {
  const isIntro = document.body.classList.contains('intro-page');
  if (!isIntro) return;

  const gallery = $('#introGallery');
  const track = $('#introTrack');
  const dotsWrap = $('#introDots');
  const prevBtn = $('#introPrev');
  const nextBtn = $('#introNext');
  const logoBtn = $('#introLogo');
  const enterBtnTop = $('#introEnter');

  if (!gallery || !track || !dotsWrap) return;

  const goMain = () => (location.href = 'main.html');
  logoBtn?.addEventListener('click', goMain);
  enterBtnTop?.addEventListener('click', goMain);

  const originals = $$('.intro-card', track);
  const realCount = originals.length;
  if (realCount < 2) return;

  const cs = getComputedStyle(track);
  const gap = parseFloat(cs.columnGap || cs.gap || '0') || 0;

  const avgW = originals.reduce((s, c) => s + c.offsetWidth, 0) / realCount;
  const cloneN = Math.max(2, Math.ceil(gallery.clientWidth / (avgW + gap)) + 1);

  const loopW =
    originals.reduce((s, c) => s + c.offsetWidth, 0) + gap * (realCount - 1);

  const headClones = [];
  const tailClones = [];

  for (let i = 0; i < cloneN; i++) {
    const last = originals[realCount - 1 - i].cloneNode(true);
    last.classList.add('is-clone');
    headClones.unshift(last);

    const first = originals[i].cloneNode(true);
    first.classList.add('is-clone');
    tailClones.push(first);
  }

  headClones.forEach((c) => track.insertBefore(c, track.firstChild));
  tailClones.forEach((c) => track.appendChild(c));

  const cardsAll = $$('.intro-card', track);
  const realCards = cardsAll.slice(cloneN, cloneN + realCount);

  dotsWrap.innerHTML = '';
  realCards.forEach((_, i) => {
    const b = document.createElement('button');
    b.type = 'button';
    b.className = 'intro-dot';
    b.addEventListener('click', () => scrollToReal(i, true));
    dotsWrap.appendChild(b);
  });
  const dots = $$('.intro-dot', dotsWrap);

  const centerLeft = (card) => card.offsetLeft + card.offsetWidth / 2;

  function scrollToCard(card, smooth) {
    const left = centerLeft(card) - gallery.clientWidth / 2;
    gallery.scrollTo({ left, behavior: smooth ? 'smooth' : 'auto' });
  }
  function scrollToReal(i, smooth) {
    scrollToCard(realCards[i], smooth);
  }

  scrollToReal(0, false);

  function activeRealIndex() {
    const cx = gallery.scrollLeft + gallery.clientWidth / 2;
    let best = 0;
    let bestDist = Infinity;

    realCards.forEach((card, i) => {
      const d = Math.abs(cx - centerLeft(card));
      if (d < bestDist) {
        bestDist = d;
        best = i;
      }
    });

    return best;
  }

  let prevActive = null;

  function updateUI() {
    const ri = activeRealIndex();
    const active = realCards[ri];

    if (prevActive && prevActive !== active) {
      prevActive.classList.add('is-leaving');
      setTimeout(() => prevActive.classList.remove('is-leaving'), 550);
    }

    dots.forEach((d, i) => d.classList.toggle('is-active', i === ri));

    const cx = gallery.scrollLeft + gallery.clientWidth / 2;
    let bestAll = 0;
    let bestAllDist = Infinity;

    cardsAll.forEach((card, i) => {
      const d = Math.abs(cx - centerLeft(card));
      if (d < bestAllDist) {
        bestAllDist = d;
        bestAll = i;
      }
    });

    cardsAll.forEach((c, i) => c.classList.toggle('is-active', i === bestAll));

    prevActive = active;
    return ri;
  }

  function fixLoop() {
    const left = gallery.scrollLeft;
    const start = centerLeft(realCards[0]) - gallery.clientWidth / 2;
    const end = centerLeft(realCards[realCount - 1]) - gallery.clientWidth / 2;

    if (left < start - loopW * 0.5) gallery.scrollLeft += loopW;
    if (left > end + loopW * 0.5) gallery.scrollLeft -= loopW;
  }

  updateUI();

  let snapTimer = null;
  gallery.addEventListener('scroll', () => {
    updateUI();
    clearTimeout(snapTimer);
    snapTimer = setTimeout(() => {
      fixLoop();
      updateUI();
    }, 120);
  });

  const goSlide = (dir) => {
    const i = updateUI();
    let next = i + dir;
    if (next < 0) next = realCount - 1;
    if (next >= realCount) next = 0;
    scrollToReal(next, true);
  };

  prevBtn?.addEventListener('click', () => goSlide(-1));
  nextBtn?.addEventListener('click', () => goSlide(1));

  gallery.addEventListener(
    'wheel',
    (e) => {
      e.preventDefault();
      gallery.scrollLeft += e.deltaY * 1.1;
    },
    { passive: false }
  );

  let down = false;
  let sx = 0;
  let sLeft = 0;

  gallery.addEventListener('pointerdown', (e) => {
    down = true;
    sx = e.clientX;
    sLeft = gallery.scrollLeft;
    gallery.setPointerCapture(e.pointerId);
    resetAllTilt();
  });

  gallery.addEventListener('pointermove', (e) => {
    if (!down) return;
    gallery.scrollLeft = sLeft - (e.clientX - sx);
  });

  gallery.addEventListener('pointerup', () => (down = false));
  gallery.addEventListener('pointercancel', () => (down = false));

  const MAX_TILT = 10;
  const IMG_SHIFT = 14;
  const HOVER_SCALE = 1.03;

  function applyTilt(card, x, y) {
    const frame = $('.intro-card-frame', card);
    const img = frame?.querySelector('img');
    if (!frame || !img) return;

    const r = frame.getBoundingClientRect();
    const cx = r.left + r.width / 2;
    const cy = r.top + r.height / 2;

    const dx = (x - cx) / (r.width / 2);
    const dy = (y - cy) / (r.height / 2);

    frame.style.transform = `rotateX(${(-dy * MAX_TILT).toFixed(
      2
    )}deg) rotateY(${(dx * MAX_TILT).toFixed(2)}deg) scale(${HOVER_SCALE})`;

    const baseRot =
      getComputedStyle(img).getPropertyValue('--base-rot')?.trim() || '0deg';

    img.style.transform = `translate(-50%, -55%) rotate(${baseRot}) translate(${(
      dx * IMG_SHIFT
    ).toFixed(2)}px, ${(dy * IMG_SHIFT).toFixed(2)}px)`;
  }

  function resetTilt(card) {
    const frame = $('.intro-card-frame', card);
    const img = frame?.querySelector('img');
    if (!frame || !img) return;
    frame.style.transform = '';
    img.style.transform = '';
  }

  function resetAllTilt() {
    realCards.forEach(resetTilt);
  }

  realCards.forEach((card) => {
    card.addEventListener('pointermove', (e) => {
      if (!card.classList.contains('is-active')) return resetTilt(card);
      applyTilt(card, e.clientX, e.clientY);
    });
    card.addEventListener('pointerleave', () => resetTilt(card));
  });
});

